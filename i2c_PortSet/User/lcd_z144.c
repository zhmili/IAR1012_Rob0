{
    u8 VH,VL;
	u16 i,j;
	VH=Data>>8;
	VL=Data;	
	Address_set(0,0,LCD_X_MAX-1,LCD_Y_MAX-1);
        
        for(i=0;i<LCD_X_MAX;i++)
	{
	  for (j=0;j<LCD_Y_MAX;j++)
	   {
        	LCD_DataWrite(VH);
	        LCD_DataWrite(VL);	
	   }
	 }
}


