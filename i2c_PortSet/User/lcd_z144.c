/*
*********************************************************************************************************
*	  : lcd_config
*	: LCD IO
*	    
*	  : ()
*********************************************************************************************************
*/
void lcd_config()
{
	GPIO_InitTypeDef GPIO_InitStructure;
	
	//  IO
	RCC_APB2PeriphClockCmd ( RCC_APB2Periph_GPIOB, ENABLE );
	
	//  IO 							nCS					nRET			  SCK						A0				SDA
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_10|GPIO_Pin_11|GPIO_Pin_13|GPIO_Pin_14|GPIO_Pin_15;
	GPIO_InitStructure.GPIO_Speed=GPIO_Speed_2MHz;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;
	//   IO
	GPIO_Init(GPIOB, &GPIO_InitStructure);		
}


//========================================================================
// : void LCD_DataWrite(unsigned int Data)
// : LCDRAM
// : Data  
// : 
// : 
// :
//      2007/01/09      First version
//========================================================================
void LCD_DataWrite(unsigned char Dat)
{
	unsigned char Num;
	LCD_CS(0);
	LCD_A0(1);
            
	for(Num=0;Num<8;Num++)
	{
		if((Dat&0x80) == 0)	
                {
                  LCD_SDI(0);
                }
		else 
                {
                  LCD_SDI(1);
                }
		Dat = Dat << 1;
		LCD_CLK(0);
		LCD_CLK(1);
	}
        LCD_CS(1);
}


//========================================================================
// : void LCD_RegWrite(unsigned char Command)
// : LCD
// : Command		byte 
// : 
// : 
// :
//      2007/01/09      First version
//========================================================================
void LCD_RegWrite(unsigned char Command)
{
	unsigned char Num;
	LCD_CS(0);//LCD_CS(0);//LCD_CS = 0;
	LCD_A0(0);//LCD_A0(0);//LCD_A0 = 0;
 
	for(Num=0;Num<8;Num++)
	{
		if((Command&0x80) == 0)	
                {
                  LCD_SDI(0);//LCD_SDI(0);//LCD_SDI = 0;
                }
		else 
                {
                 LCD_SDI(1);//LCD_SDI(1);//LCD_SDI = 1;
                }
		Command = Command << 1;
		LCD_CLK(0);//LCD_CLK(0);//LCD_CLK = 0;
		LCD_CLK(1);//LCD_CLK(1);//LCD_CLK = 1;
	}
	LCD_CS(1);//LCD_CS(1);//LCD_CS = 1;
}

