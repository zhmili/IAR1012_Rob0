void UART5_IRQHandler(void)
{
	int i;
	
	if(USART_GetITStatus(UART5, USART_IT_RXNE) != RESET)
	{		
	uart_rec = uart_read();
	uart_buf[get_lenth] = uart_rec;
	
	get_lenth++;
	if(get_lenth > 5)
	{
		printf("please set less than 9999E, try again!\n\r",get_id);
		get_lenth = 0;
	}
	else if((uart_rec == 'E'))
	{
    if(get_lenth == 0)
		{
			get_id = 0;
		}
		else if(get_lenth == 1){
			get_id = uart_buf[0] - '0';
		}
		else if(get_lenth == 2){
			get_id = (uart_buf[0] - '0')*10 + (uart_buf[1] - '0');
		}
		else if(get_lenth == 3){
			get_id = (uart_buf[0] - '0')*100 + (uart_buf[1] - '0')*10 + (uart_buf[3] - '0');
		}
		else if(get_lenth == 4){
			get_id = (uart_buf[0] - '0')*1000 + (uart_buf[1] - '0')*100 + (uart_buf[2] - '0')*10 + (uart_buf[3] - '0');
		}
		
    uart_rec = 0;
		get_lenth = 0;
		printf("get dt1:%d\n\r",get_id);
		set_ready = 1;
	}
	}
}

