#ifndef __RTC_H
#define __RTC_H
#include <time.h>


/********************************************************************************
  函数: ReadRealTime
  描述: 得到系统时钟
********************************************************************************/
extern BOOLEAN ReadRealTime(struct tm *pstcReg);

/*********************************************************************************************************
** 函数名称: IsRealTimeActive
** 功能描述: 判断实时时钟是否已在运行
** 输　入:   无
** 输　出:   无
** 返回值:   FALSE: 实时时钟尚未运行, 需重新设置新的时间
             TRUE:  实时时钟已运行
********************************************************************************************************/
extern BOOLEAN IsRealTimeActive(void);

/*********************************************************************************************************
** 函数名称: SetRealTime
** 功能描述: 设置实时时钟的时钟值
** 输　入:   pstcReg 要设置的时钟值结构
** 输  出:   无
** 返回值:   TRUE:  设置时钟成功
             FALSE: 设置时钟失败
********************************************************************************************************/
extern BOOLEAN SetRealTime(const struct tm *pstcReg);

/*********************************************************************************************************
** 函数名称: SaveParaToRTC
** 功能描述: 将4字节的值保存在RTC中, 作为掉电保存参数
********************************************************************************************************/
extern void SaveParaToRTC(INT32U dwPara);

/*********************************************************************************************************
** 函数名称: RestoreParaFromRTC
** 功能描述: 从RTC中读取4字节的掉电保存参数
********************************************************************************************************/
extern INT32U RestoreParaFromRTC(void);


/*********************************************************************************************************
** 函数名称: RTC_Configuration
** 功能描述: 时钟配置初始化
********************************************************************************************************/
extern void RTC_Configuration(void);


#endif
